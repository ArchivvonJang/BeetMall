package com.beetmall.sshj.custom.vo;

public class ChatVO {
	private int otonum;
	private int roomcode;
	private String receiver;
	private String sender;
	private String otodate;
	private String otocontent;
	public int getOtonum() {
		return otonum;
	}
	public void setOtonum(int otonum) {
		this.otonum = otonum;
	}
	public int getRoomcode() {
		return roomcode;
	}
	public void setRoomcode(int roomcode) {
		this.roomcode = roomcode;
	}
	public String getReceiver() {
		return receiver;
	}
	public void setReceiver(String receiver) {
		this.receiver = receiver;
	}
	public String getSender() {
		return sender;
	}
	public void setSender(String sender) {
		this.sender = sender;
	}
	public String getOtodate() {
		return otodate;
	}
	public void setOtodate(String otodate) {
		this.otodate = otodate;
	}
	public String getOtocontent() {
		return otocontent;
	}
	public void setOtocontent(String otocontent) {
		this.otocontent = otocontent;
	}
	
	
}
