package com.beetmall.sshj.custom.vo;

public class PopupVO {
	private String popupimg;
	private String popuplink;
	private int popupterm;
	private int popupheight;
	private int popupwidth;
	public String getPopupimg() {
		return popupimg;
	}
	public void setPopupimg(String popupimg) {
		this.popupimg = popupimg;
	}
	public String getPopuplink() {
		return popuplink;
	}
	public void setPopuplink(String popuplink) {
		this.popuplink = popuplink;
	}
	public int getPopupterm() {
		return popupterm;
	}
	public void setPopupterm(int popupterm) {
		this.popupterm = popupterm;
	}
	public int getPopupheight() {
		return popupheight;
	}
	public void setPopupheight(int popupheight) {
		this.popupheight = popupheight;
	}
	public int getPopupwidth() {
		return popupwidth;
	}
	public void setPopupwidth(int popupwidth) {
		this.popupwidth = popupwidth;
	}
	

}
